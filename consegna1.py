def frequenza(stringa):
  diz={}
  for c in stringa:
    if diz.get(c,-1)!=-1:
      diz[c]=diz[c]+1
    else:
      diz[c]=1
      
  return diz

#prove
print(frequenza("aba bcc"))
print(frequenza("pippo e pluto"))
print(frequenza("PAoLa e pAolO @23"))
print(frequenza("     "))#5 spazi
print(frequenza(""))
