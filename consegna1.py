def conta_vocali(stringa):
  
  if len(stringa)==0:
    return 0

  contaVocali=0
  vocali=("a","e","i","o","u","A","E","I","O","U","à","è","ì","ò","ù","À","È","Ì","Ò","Ù","á","é","í","ó","ú","Á","É","Í","Ó","Ú")
  for c in stringa:
    if c in vocali:
      contaVocali=contaVocali+1
  return contaVocali

#prove
print(conta_vocali("pippo"))#2
print(conta_vocali("paperino"))#4
print(conta_vocali("plutO"))#2
print(conta_vocali(""))#0
print(conta_vocali("pippo pluto paperino"))#8
print(conta_vocali("abcd"))#1
print(conta_vocali("qwrtp"))#0
print(conta_vocali("aaAa"))#4
print(conta_vocali("PIPPO pippo PLUTO PaApà"))#8
print(conta_vocali("aeiouAEIOUàèìòùÀÈÌÒÙáéíóúÁÉÍÓÚ"))#30

