def divisori(lista_numeri):
  if len(lista_numeri)==0:
    return 0

  conta_divisori=0
  for i in range(len(lista_numeri)):
    if lista_numeri[i]!=0:
      for j in range(len(lista_numeri)):
        if i!=j and lista_numeri[j]%lista_numeri[i]==0:
          conta_divisori=conta_divisori+1
          break
  return conta_divisori

#prove
lista=[-3,-3,4,2,1]
